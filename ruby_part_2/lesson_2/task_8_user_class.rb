class User
  def set_f_name(f_name)
    @f_name = f_name
  end

  def f_name
    @f_name
  end

  def set_s_name(s_name)
    @s_name = s_name
  end

  def s_name
    @s_name
  end

  def set_t_name(t_name)
    @t_name = t_name
  end

  def t_name
    @t_name
  end

  def set_score(score)
    @score = score
  end

  def score
    @score
  end
end
